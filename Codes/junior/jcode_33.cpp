#include <iostream> // подключаем библиотеку для ввода/вывода

int main() { // начало функции main
    int x = 5; // объявляем переменную x и присваиваем ей значение 5
    std::cout << "x = " << x << std::endl; // выводим значение x на экран
    x = 10; // изменяем значение x
    std::cout << "x = " << x << std::endl; // выводим новое значение x на экран
    return 0; // завершаем функцию main с кодом возврата 0
}
